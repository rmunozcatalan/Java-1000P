    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        // TODO add your handling code here:

        System.out.println("Agregar");

        //El código de producto debe ser un número entre 1000 y 9999
        try {
            if (Integer.parseInt(this.txtCodigo.getText()) < 1000
                    && Integer.parseInt(this.txtCodigo.getText()) > 9999) {
                System.out.println("El código de producto debe ser un número entre 1000 y 9999.");
                JOptionPane.showMessageDialog(
                        null,
                        "El código de producto debe ser un número entre 1000 y 9999.",
                        "AVISO DE SISTEMA",
                        JOptionPane.PLAIN_MESSAGE
                );
            }
        } catch (Exception e) {
            System.out.println("Mensaje " + e.getMessage());;
        }
        //El nombre del producto debe tener más de 2 caracteres
        try {
            if (this.txtNombre.getText().length() < 2) {
                System.out.println("El nombre del producto debe tener más de 2 caracteres.");

                JOptionPane.showMessageDialog(
                        null,
                        "El nombre del producto debe tener más de 2 caracteres.",
                        "AVISO DE SISTEMA",
                        JOptionPane.PLAIN_MESSAGE
                );
            }
        } catch (Exception e) {
            System.out.println("Mensaje " + e.getMessage());;
        }
        //El precio del producto debe ser mayor a 0
        try {
            if (Integer.parseInt(this.txtPrecio.getText()) <= 0) {
                System.out.println("El precio del producto debe ser mayor a 0.");
                JOptionPane.showMessageDialog(
                        null,
                        "El precio del producto debe ser mayor a 0.",
                        "AVISO DE SISTEMA",
                        JOptionPane.PLAIN_MESSAGE
                );
            }
        } catch (Exception e) {
            System.out.println("Mensaje " + e.getMessage());;
        }

        if (txtTipoProducto.getText().equals("Medicamento")) {

            boolean bMedicamentoBioEquivalente = false;
            if (txtMedicamentoBioEquivalente.getText().equals("BioEquivalente")) {
                bMedicamentoBioEquivalente = true;
            }
            Medicamento oMedicamento = new Medicamento(
                    Integer.parseInt(this.txtCodigo.getText()) // CODIGO
                    ,
                     txtNombre.getText() // NOMBRE
                    ,
                     Integer.parseInt(txtPrecio.getText()) // PRECIO
                    ,
                     bMedicamentoBioEquivalente // BIOEQUIVALENTE
            );

            // Si el producto es bioequivalente, se le aplica al precio el descuento correspondiente
            if (bMedicamentoBioEquivalente) {
                oMedicamento.setPrecio(oMedicamento.getPrecio() - (int) oMedicamento.calcularPrecioOferta());
            }
            productos.add(iContadorProductos, oMedicamento);
            iContadorProductos++;
            System.out.println("Medicamento Agregado");
        }

        if (txtTipoProducto.getText().equals("Complemento")) {

            //La cantidad de componentes debe ser mayor a 0
            try {
                if (Integer.parseInt(this.txtCantidadComplemento.getText()) <= 0) {
                    System.out.println("La cantidad de componentes debe ser mayor a 0.");
                    JOptionPane.showMessageDialog(
                            null,
                            "La cantidad de componentes debe ser mayor a 0.",
                            "AVISO DE SISTEMA",
                            JOptionPane.PLAIN_MESSAGE
                    );
                }
            } catch (Exception e) {
                System.out.println("Mensaje " + e.getMessage());;
            }

            int txtCantidadComplemento = 0;

            Complemento oComplemento = new Complemento(
                    Integer.parseInt(this.txtCodigo.getText()) // CODIGO
                    ,
                     this.txtNombre.getText() // NOMBRE
                    ,
                     Integer.parseInt(this.txtPrecio.getText()) // PRECIO
                    ,
                     Integer.parseInt(this.txtCantidadComplemento.getText()) // CANTIDAD COMPLEMENTO
            );

            // Si la cantidad de componentes del producto es menor a 5, se aplica al precio el descuento correspondiente.
            if (oComplemento.getCantidadComponentes() < 5) {
                oComplemento.setPrecio(
                        oComplemento.getPrecio() - (int) oComplemento.calcularPrecioOferta()
                );
            }
            productos.add(iContadorProductos, oComplemento);
            iContadorProductos++;
            System.out.println("Complemento Agregado");
        }
    }//GEN-LAST:event_btnAgregarProductoActionPerformed

