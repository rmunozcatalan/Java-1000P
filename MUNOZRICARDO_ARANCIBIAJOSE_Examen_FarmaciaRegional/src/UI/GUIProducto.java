    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:

        System.out.println("Listar");
        String sDetalle;

        for (int i = 0; i < 10; i++) {
            tblProductos.setValueAt("", i, 0);
            tblProductos.setValueAt("", i, 1);
            tblProductos.setValueAt("", i, 2);
            tblProductos.setValueAt("", i, 3);
            tblProductos.setValueAt("", i, 4);
        }

        System.out.println("productos.toString() " + productos.toString());

        for (int i = 0; i < productos.size(); i++) {

            tblProductos.setValueAt(productos.get(i).getCodigo(), i, 0);
            tblProductos.setValueAt(productos.get(i).getNombre(), i, 1);

            tblProductos.setValueAt(productos.get(i).getPrecio(), i, 2);
            //            int precioConOferta = productos.get(i).getPrecio() - (int) productos.get(i).calcularPrecioOferta();
            //            tblProductos.setValueAt(precioConOferta, i, 2);

            if ((Producto) productos.get(i) instanceof Medicamento) {
                tblProductos.setValueAt("Medicamento", i, 3);
                if (((Medicamento) productos.get(i)).isBioequivalente()) {
                    sDetalle = "Bioequivalente";
                } else {
                    sDetalle = "No Bioequivalente";
                }

                tblProductos.setValueAt(sDetalle, i, 4);
            }
            if ((Producto) productos.get(i) instanceof Complemento) {
                tblProductos.setValueAt("Complemento", i, 3);
                tblProductos.setValueAt(((Complemento) productos.get(i)).getCantidadComponentes(), i, 4);
                //                tblProductos.setValueAt(((Complemento) productos.get(i)).getCantidadComponentes(), i, 4);
            }
        }
    }//GEN-LAST:event_btnListarActionPerformed

