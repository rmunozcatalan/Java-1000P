     public void llenarTabla() throws SQLException {
 
        JTable tabla;
        ResultSet rs;
        DefaultTableModel dfm = new DefaultTableModel();
        tabla = this.tablaPeliculas;
        tabla.setModel(dfm);
         
        dfm.setColumnIdentifiers(new Object[]{"Codigo","Precio","Idcategoria","Formato 4K","Nombre"});
        ControlPelicula rp = new ControlPelicula();
        rs = rp.listarPeliculasPorPrecio();
        
        try {
            while(rs.next()){
                dfm.addRow(new Object[]{rs.getInt("codigo"), rs.getInt("precio"), rs.getInt("idcategoria"), rs.getString("formato4k"), rs.getString("nombre")});
            }
        } catch (Exception e) {
            
        }
         
    }
