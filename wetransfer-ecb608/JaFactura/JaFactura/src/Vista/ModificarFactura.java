    private void btnModificarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarFacturaActionPerformed
        Factura factura=new Factura();
        int numero=Integer.parseInt(this.txtNroFactura.getText().trim());
        factura.setRut_cliente(this.txtRutCliente.getText().trim());
        factura.setMonto(Integer.parseInt(txtMonto.getText()));
        if (new Controlador.PersistenciaFactura().ModificaFactura(numero, factura)) {
            javax.swing.JOptionPane.showMessageDialog(this,"registro ingresado");
            
        }else{
            javax.swing.JOptionPane.showMessageDialog(this,"registro no ingresado");
        }
    }//GEN-LAST:event_btnModificarFacturaActionPerformed

