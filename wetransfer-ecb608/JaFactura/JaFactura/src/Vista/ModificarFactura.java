    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int numero =Integer.parseInt(txtNroFactura.getText().trim());//trim elimina los espacion en blanco
        Factura factura=new PersistenciaFactura().buscarPorNumero(numero);
        if (factura==null) {
            javax.swing.JOptionPane.showMessageDialog(this,"no hay registro");
        }else{
            this.txtRutCliente.setText(factura.getRut_cliente());
            this.txtMonto.setText(String.valueOf(factura.getMonto()));
            this.txtFecha.setText(String.valueOf(factura.getFecha()));

        }
    }//GEN-LAST:event_btnBuscarActionPerformed

